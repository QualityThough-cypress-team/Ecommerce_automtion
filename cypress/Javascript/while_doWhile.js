// Example of a do...while loop
let number = 4;
console.log("\nDo...While Loop:");
do {
    console.log("Number is:", number);
    number++;
} while (number < 10);
// The do...while loop will execute the block of code 
// once before checking the condition
// and then continue to execute the block as long as the condition is true
// The loop will stop when the condition is false